# Step 1: Install the GNOME Shell integration browser extension
# Visit the following URL and add the extension to your browser:
# https://chrome.google.com/webstore/detail/gnome-shell-integration/gphhapmejobijbbhgpjhcjognlahblep

# Step 2: Install the GNOME Shell host connector
sudo apt update && sudo apt install chrome-gnome-shell

# Step 3: Install the Clipboard Indicator extension
# Open this link and toggle on the Clipboard Indicator:
# https://extensions.gnome.org/extension/779/clipboard-indicator/

# Troubleshooting Steps:
# If you encounter any issues, try the following steps one at a time:

# 1. Clear your browser's cache and cookies, then restart the browser.

# 2. Ensure that the GNOME Shell integration extension is enabled in your browser's extension settings.

# 3. Restart your GNOME Shell without logging out or rebooting by pressing Alt+F2, typing 'r', and pressing Enter.

# 4. If the problem persists, restart your machine.

# 5. Try using a different browser like Firefox, Edge, or Brave to see if the issue is browser-specific.

# Note: For browsers like Firefox, you may need to install a different host connector. Use your package manager to search for and install the appropriate package, e.g., 'firefox-gnome-shell'.
